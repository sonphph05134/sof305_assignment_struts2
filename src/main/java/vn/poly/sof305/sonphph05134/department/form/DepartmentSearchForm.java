package vn.poly.sof305.sonphph05134.department.form;



/**
 * @author Hoai Son
 *
 */
public class DepartmentSearchForm {

	private String code;

	private String name;
	
	private Long pn;

	/**
	 * @return the code
	 */
	public String getCode() {
		return code;
	}

	/**
	 * @param code the code to set
	 */
	public void setCode(String code) {
		this.code = code;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the pn
	 */
	public Long getPn() {
		return pn;
	}

	/**
	 * @param pn the pn to set
	 */
	public void setPn(Long pn) {
		this.pn = pn;
	}
	
	
}
